/**
 * POST /api/reports/generate
 * Generates a monthly AI report for the authenticated user.
 * Body: { year: number, month: number, csvData?: object (premium only) }
 *
 * Data: journal_trades + journal_daily + ai_chart_checks + aura_analysis_trades (Trade Validator)
 * Tone: clinical, harsh-on-behaviour coaching; mandatory negatives; DNA vs monthly split explained.
 */

const { verifyToken } = require('../utils/auth');
const { executeQuery } = require('../db');
const { getPerplexityModelForReports } = require('../ai/perplexity-config');
const { getReportDataSpanDays } = require('./dataSpan');
const { applyScheduledDowngrade } = require('../utils/apply-scheduled-downgrade');
const { effectiveReportsRole } = require('./resolveReportsRole');

const PERPLEXITY_API_KEY = process.env.PERPLEXITY_API_KEY;
const PERPLEXITY_MODEL = getPerplexityModelForReports();
const MIN_DATA_DAYS = 30;

const DISCLAIMER_TEXT =
  'This report is generated by AI from your trading and platform data. It is for educational and self-improvement only. It is not financial, medical, or psychological advice. Feedback addresses trading behaviour and process, not your worth as a person. Past performance does not guarantee future results. If you are in distress, seek a qualified professional.';

function monthName(m) {
  return [
    'January',
    'February',
    'March',
    'April',
    'May',
    'June',
    'July',
    'August',
    'September',
    'October',
    'November',
    'December',
  ][m - 1];
}

// ── Data aggregation ──────────────────────────────────────────────────────────

async function aggregateJournalTrades(userId, year, month) {
  const startDate = `${year}-${String(month).padStart(2, '0')}-01`;
  const endDate = new Date(year, month, 0).toISOString().split('T')[0];
  const [rows] = await executeQuery(
    `SELECT pair, tradeType, session, riskPct, rResult, dollarResult,
            followedRules, notes, emotional, date
     FROM journal_trades
     WHERE userId = ? AND date BETWEEN ? AND ?
     ORDER BY date`,
    [userId, startDate, endDate]
  ).catch(() => [[]]);
  return rows || [];
}

/** Trade Validator / Aura Analysis — same window as monthly report */
async function aggregateAuraAnalysisTrades(userId, year, month) {
  const startDate = `${year}-${String(month).padStart(2, '0')}-01`;
  const endDate = new Date(year, month, 0).toISOString().split('T')[0];
  const [rows] = await executeQuery(
    `SELECT pair, session, result, pnl, r_multiple, risk_percent, rr, checklist_percent, created_at
     FROM aura_analysis_trades
     WHERE user_id = ?
       AND result IN ('win','loss','breakeven')
       AND DATE(created_at) BETWEEN ? AND ?
     ORDER BY created_at ASC`,
    [userId, startDate, endDate]
  ).catch(() => [[]]);
  return rows || [];
}

