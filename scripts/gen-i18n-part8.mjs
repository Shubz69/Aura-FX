/**
 * Builds scripts/i18n-part8.mjs from XP matrix + profile/navbar seed rows.
 * Run: node scripts/gen-i18n-part8.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { TRADING_RANKS, XP_TIER_NAMES_EN } from '../src/utils/xpSystem.js';
import { XP_MATRIX_BY_LANG, XP_TIERS_BY_LANG } from './xp-matrix-translations.mjs';
import { rows as profileRows } from './i18n-part8-profile-rows.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const LANGS = ['en', 'zh-CN', 'hi', 'es', 'fr', 'ar', 'bn', 'pt', 'ru', 'ur'];

for (const code of LANGS.slice(1)) {
  const m = XP_MATRIX_BY_LANG[code];
  if (!m || m.length !== 10 || m.some((r) => r.length !== 10)) {
    throw new Error(`XP_MATRIX_BY_LANG invalid for ${code}`);
  }
  const t = XP_TIERS_BY_LANG[code];
  if (!t || t.length !== 10) throw new Error(`XP_TIERS_BY_LANG invalid for ${code}`);
}

const xpRows = [];
for (let ti = 0; ti < 10; ti += 1) {
  xpRows.push({
    k: `xp.tier.${ti}`,
    en: XP_TIER_NAMES_EN[ti],
    'zh-CN': XP_TIERS_BY_LANG['zh-CN'][ti],
    hi: XP_TIERS_BY_LANG.hi[ti],
    es: XP_TIERS_BY_LANG.es[ti],
    fr: XP_TIERS_BY_LANG.fr[ti],
    ar: XP_TIERS_BY_LANG.ar[ti],
    bn: XP_TIERS_BY_LANG.bn[ti],
    pt: XP_TIERS_BY_LANG.pt[ti],
    ru: XP_TIERS_BY_LANG.ru[ti],
    ur: XP_TIERS_BY_LANG.ur[ti],
  });
}

const levels = Object.keys(TRADING_RANKS)
  .map(Number)
  .sort((a, b) => a - b);
for (const lv of levels) {
  const ti = Math.floor((lv - 1) / 10);
  const pos = (lv - 1) % 10;
  xpRows.push({
    k: `xp.rank.${lv}`,
    en: TRADING_RANKS[lv],
    'zh-CN': XP_MATRIX_BY_LANG['zh-CN'][ti][pos],
    hi: XP_MATRIX_BY_LANG.hi[ti][pos],
    es: XP_MATRIX_BY_LANG.es[ti][pos],
    fr: XP_MATRIX_BY_LANG.fr[ti][pos],
    ar: XP_MATRIX_BY_LANG.ar[ti][pos],
    bn: XP_MATRIX_BY_LANG.bn[ti][pos],
    pt: XP_MATRIX_BY_LANG.pt[ti][pos],
    ru: XP_MATRIX_BY_LANG.ru[ti][pos],
    ur: XP_MATRIX_BY_LANG.ur[ti][pos],
  });
}

const all = [...profileRows, ...xpRows];
const seen = new Set();
for (const r of all) {
  if (seen.has(r.k)) throw new Error(`Duplicate key ${r.k}`);
  seen.add(r.k);
}

const header = `/** Generated by scripts/gen-i18n-part8.mjs — do not hand-edit; re-run generator after changing seeds. */
/** @typedef {{ k: string, en: string, 'zh-CN': string, hi: string, es: string, fr: string, ar: string, bn: string, pt: string, ru: string, ur: string }} Row */
/** @type {Row[]} */
/* eslint-disable max-len */
`;

const body = `${header}export const rows = ${JSON.stringify(all, null, 2)};\n`;
fs.writeFileSync(path.join(__dirname, 'i18n-part8.mjs'), body, 'utf8');
console.log('Wrote i18n-part8.mjs with', all.length, 'rows (profile/navbar:', profileRows.length, ', xp:', xpRows.length, ')');
