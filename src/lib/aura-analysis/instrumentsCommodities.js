/**
 * Commodity CFD / futures-style specs for the risk calculator.
 * examplePrices: realistic BUY setup (stop below entry, TP above) at typical quote scales.
 * Contract sizes follow common MT5/CFD conventions; broker variants may differ.
 */

/** @typedef {'metal'|'energy'|'agriculture'|'softs'|'other'} CommoditySubCategory */

function commodityRow(symbol, displayName, opts) {
  const {
    examplePrices,
    contractSize,
    tickSize,
    pricePrecision = 2,
    pointSize,
    minReasonablePrice,
    maxReasonablePrice,
    lotStep = 0.01,
    minLot = 0.01,
    maxLot = 100,
    /** @type {CommoditySubCategory|undefined} */
    subCategory,
    /** 'decimal' | 'points' — display/calc hint; commodity calculator uses contractSize × price move */
    priceFormat = 'decimal',
  } = opts;
  return {
    symbol,
    displayName,
    assetClass: 'commodity',
    calculationMode: 'commodity',
    quoteCurrency: 'USD',
    contractSize,
    tickSize,
    pricePrecision,
    pointSize: pointSize ?? 1,
    lotStep,
    minLot,
    maxLot,
    minReasonablePrice,
    maxReasonablePrice,
    examplePrices,
    subCategory: subCategory || 'other',
    priceFormat,
  };
}

